def calculator_tool(expression: str):
    try:
        return str(eval(expression))
    except:
        return "Invalid expression"

def symptom_checker(symptoms: str):

    symptoms = symptoms.lower()

    if "chest pain" in symptoms and "breathing" in symptoms:
        return "HIGH RISK: possible cardiac or respiratory emergency"

    if "fever" in symptoms and "cough" in symptoms:
        return "Possible infection (TB / flu / pneumonia)"

    return "Low risk but monitor symptoms"

def tool_router(state):

    query = state["patient_query"]

    if "calculate" in query:
        return "calculator"

    if "symptom" in query:
        return "symptom_checker"

    return "medical"

def structured_medical_output(state):

    prompt = f"""
    Convert this into a medical report JSON:

    {state['medical_response']}

    Return ONLY JSON with:

    {{
      "risk_level": "",
      "possible_conditions": [],
      "recommended_actions": [],
      "follow_up_days": number,
      "emergency": true/false
    }}
    """

    response = llm.invoke(prompt)

    return {"final_response": response.content}

def hallucination_checker(state):

    prompt = f"""
    You are a medical safety validator.

    Check if this response is medically unsafe or hallucinated:

    RESPONSE:
    {state['medical_response']}

    Rules:
    - No fake drugs
    - No false diagnosis
    - Must align with general medical guidelines

    Return:
    SAFE or UNSAFE + reason
    """

    result = llm.invoke(prompt)

    return {"evaluation": result.content}

def safety_router(state):

    if "UNSAFE" in state.get("evaluation", ""):
        return "retriage"

    return "final"


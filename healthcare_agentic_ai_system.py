def supervisor(state):

    query = state["patient_query"].lower()

    if "calculate" in query:
        return "tool"

    if "pain" in query or "breathing" in query:
        return "triage"

    return "medical"

def reflection_agent(state):

    prompt = f"""
    Evaluate this medical response:

    {state['medical_response']}

    Check:
    - safety
    - hallucinations
    - missing warnings

    Return: SAFE or FIX_REQUIRED
    """

    result = llm.invoke(prompt)

    return {"evaluation": result.content}

def safety_router(state):

    if "FIX_REQUIRED" in state["evaluation"]:
        return "medical"   # loop back

    return "final"

def tool_agent(state):

    query = state["patient_query"]

    if "calculate" in query:
        result = eval(query.replace("calculate", ""))

        return {"tool_result": str(result)}

    return {"tool_result": "No tool used"}


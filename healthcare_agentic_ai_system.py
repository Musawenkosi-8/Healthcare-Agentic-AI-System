class HealthcareState(TypedDict):
    patient_query: str
    triage_result: str
    medical_response: str
    followup_plan: str

def triage_agent(state: HealthcareState):

    query = state["patient_query"]

    prompt = f"""
    You are a healthcare triage assistant.

    Classify the patient into:

    - EMERGENCY
    - URGENT
    - ROUTINE

    Symptoms:

    {query}

    Return:
    Risk Level
    Explanation
    """

    response = llm.invoke(prompt)

    return {
        "triage_result": response.content
    }

def medical_agent(state: HealthcareState):

    query = state["patient_query"]

    prompt = f"""
    You are a medical assistant for South Africa.

    Patient query:

    {query}

    Provide:

    - Possible causes
    - Recommended actions
    - Warning signs

    Do NOT diagnose.
    """

    response = llm.invoke(prompt)

    return {
        "medical_response": response.content
    }

def followup_agent(state: HealthcareState):

    prompt = f"""
    Based on this triage result:

    {state['triage_result']}

    Create:

    - Follow-up instructions
    - Medication reminder suggestions
    - Next appointment recommendation
    """

    response = llm.invoke(prompt)

    return {
        "followup_plan": response.content
    }

def route_patient(state):

    triage = state["triage_result"].upper()

    if "EMERGENCY" in triage:
        return "emergency"

    return "normal"

def emergency_agent(state: HealthcareState):

    return {
        "medical_response":
        """
        EMERGENCY DETECTED

        Seek immediate medical attention.
        Call emergency services.
        Visit nearest hospital immediately.
        """
    }

workflow = StateGraph(HealthcareState)

workflow.add_node("triage", triage_agent)
workflow.add_node("medical", medical_agent)
workflow.add_node("followup", followup_agent)
workflow.add_node("emergency", emergency_agent)

workflow.set_entry_point("triage")

